/**
 * 輔助函數：生成規格 ID
 */
const generateSpecId = (productId: string, index: number) =>
  `${productId}-spec-${index}`

/**
 * 輔助函數：建立規格陣列
 */
const createSpecs = (
  productId: string,
  specData: Array<{ name: string; quantity: number }>
): IProductSpec[] =>
  specData.map((spec, index) => ({
    id: generateSpecId(productId, index),
    name: spec.name,
    quantity: spec.quantity,
  }))

